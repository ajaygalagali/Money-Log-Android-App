package com.astro.rokka.OthersFeature;

public class otherMainList {
    private String otherMemName;
    private int otherMemBalance;

    public otherMainList(String otherMemName, int otherMemBalance) {
        this.otherMemName = otherMemName;
        this.otherMemBalance = otherMemBalance;
    }

    public String getOtherMemName() {
        return otherMemName;
    }

    public void setOtherMemName(String otherMemName) {
        this.otherMemName = otherMemName;
    }

    public int getOtherMemBalance() {
        return otherMemBalance;
    }

    public void setOtherMemBalance(int otherMemBalance) {
        this.otherMemBalance = otherMemBalance;
    }
}
